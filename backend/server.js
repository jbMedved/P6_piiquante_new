// importe le package HTTP de node
const http = require('http');
// on va importer le ficher app.js
const app = require('./app');

// ici on "normalise le port" afin de s'assurer 
// que le posrt soit toujours valide
const normalizePort = val => {
    const port = parseInt(val, 10);

    if (isNaN(port)) {
        return val;
    }
    if (port >= 0) {
        return port;
    }
    return false;
};

const port = normalizePort(process.env.PORT || '3000');
//on va indiquer à express (via app) sur quel port normalisé tourner
app.set('port', port);

// ici on recherche des erreurs et comment les gerer
